    private void btnchonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchonActionPerformed
        // TODO add your handling code here:
        if (txtmahd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Vui Lòng Chọn Hóa Đơn");
        } else {
            String soluongnhap = JOptionPane.showInputDialog(this , "Vui lòng nhập số lượng");
//            tt.setSoluongnhap(Integer.parseInt(soluongnhap));
//            System.out.println("soluongnhap: "+tt.getSoluongnhap());
            int index = tablesanpham.getSelectedRow();
            ModeThhanhToan dachon = splist.get(index);
            dachon.setSoluongnhap(Integer.parseInt(soluongnhap));
            dachon.setGiatheosl(dachon.getSoluongnhap() *  dachon.getGia());
            System.out.println(dachon.getGiatheosl());
            DefaultTableModel model = (DefaultTableModel) tabledachon.getModel();
            String cot1 = (String) tabledachon.getValueAt(0, 0);
            System.out.println(cot1);
            
                  
            if (cot1 == null) {
                model.setRowCount(0);
            }
            
            
            Object[] row = {
                dachon.getMaSP(), 
                dachon.getTensanpham(),
                dachon.getSoluongnhap(),
                dachon.getDonvitinh(),
                dachon.getGiatheosl()
            };
            model.addRow(row);
            int tongtien = 0;
            for (int i = 0; i < tabledachon.getRowCount(); i++) {
                Object value = tabledachon.getValueAt(i, 4);  // Lấy giá trị của cột thứ 4

                if (value != null) {
                    try {
                        tongtien += Integer.parseInt(value.toString());  // Cộng giá trị nếu không phải null
                    } catch (NumberFormatException e) {
                        e.printStackTrace();  // Xử lý nếu giá trị không thể chuyển thành số
                    }
                }
            }
            txttongtien.setText(String.valueOf(tongtien));
        }

        
               
    }//GEN-LAST:event_btnchonActionPerformed

