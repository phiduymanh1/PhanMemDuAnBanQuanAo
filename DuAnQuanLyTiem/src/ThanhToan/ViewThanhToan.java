    private void btnchonhdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchonhdActionPerformed
        // TODO add your handling code here:
        int index = tablehdcho.getSelectedRow();
        ModeThhanhToan tt = hdcholist.get(index);
        txtmahd.setText(tt.getMahd());
        DateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            cbbngaytao.setDate(dateformat.parse(tt.getNgaytao()));
        } catch (ParseException ex) {
            Logger.getLogger(ViewThanhToan.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (tt.getTthoadon().equals("ChoThanhToan")) {
            rdochothanhtoan.setSelected(true);
        } else {
            rdodathanhtoan.setSelected(true);
        }
        
    }//GEN-LAST:event_btnchonhdActionPerformed

