    public void loaddata() throws SQLException{
        hdcholist = ttsv.gethdcho();
        filltotable(hdcholist);
        splist = ttsv.loaddatasp();
        filltotablesp(splist);
    }
    public void filltotable(ArrayList<ModeThhanhToan> hdcholist){
        
        DefaultTableModel model = (DefaultTableModel) tablehdcho.getModel();
        model.setNumRows(0);
        for (ModeThhanhToan modeThhanhToan : hdcholist) {
            Object[] row = {
                modeThhanhToan.getMahd(),
                modeThhanhToan.getNgaytao(),
                modeThhanhToan.getTthoadon()
            };
            model.addRow(row);
        }
        
    }
    public void filltotablesp(ArrayList<ModeThhanhToan> splist){
        DefaultTableModel model = (DefaultTableModel) tablesanpham.getModel();
        model.setNumRows(0);
        for (ModeThhanhToan modeThhanhToan : splist) {
            Object[] row = {
                modeThhanhToan.getMaSP(), 
                modeThhanhToan.getTensanpham(),
                modeThhanhToan.getTenmau(),
                modeThhanhToan.getTenkc(),
                modeThhanhToan.getTenth(),
                modeThhanhToan.getMota(),
                modeThhanhToan.getGia(),
                modeThhanhToan.getSoluongtonkho(),
                modeThhanhToan.getDonvitinh(),
                modeThhanhToan.getTrangthai()
            };
            model.addRow(row);
        }
    }
    public void loadvocher() throws SQLException{
        
        cbbvocher.removeAllItems();
        for (ModeThhanhToan modeThhanhToan : ttsv.getvocher()) {
            cbbvocher.addItem(modeThhanhToan.getTenkm());
            vcherlist.add(modeThhanhToan);
        }      
    }
    public void tinhtien(){
        if (!txttienkhachdua.getText().isEmpty()) {
            double tienthua = Double.parseDouble(txttienkhachdua.getText().trim()) - Double.parseDouble(txtthanhtien.getText().trim());
            txttienthua.setText(String.valueOf(tienthua));
        }
    }
