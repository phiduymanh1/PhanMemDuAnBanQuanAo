    private void cbbvocherMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbbvocherMouseClicked
        // TODO add your handling code here:
        int index = cbbvocher.getSelectedIndex();
        int tongtien = Integer.parseInt(txttongtien.getText());
        ModeThhanhToan tt = vcherlist.get(index);
        if (tt.getLoaikm().equals("tienmat")) {
            txtsotiengiam.setText(String.valueOf(tt.getGiatrikm()));
        }else if (tt.getLoaikm().equals("phantram")) {
            double sotiengiam = Double.parseDouble(txttongtien.getText()) - ((tongtien * tt.getGiatrikm()) / 100);
            txtsotiengiam.setText(String.valueOf(sotiengiam));
        }
        double thanhtien = Double.parseDouble(txttongtien.getText()) - Double.parseDouble(txtsotiengiam.getText());
        txtthanhtien.setText(String.valueOf(thanhtien));
        
    }//GEN-LAST:event_cbbvocherMouseClicked

